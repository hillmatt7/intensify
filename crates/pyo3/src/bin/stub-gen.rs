fn repo_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("crates/pyo3 has a parent crates directory")
        .parent()
        .expect("crates has a parent repository directory")
        .to_path_buf()
}

fn write_stub(relative_path: &str, contents: &str) -> std::io::Result<()> {
    let path = repo_root().join(relative_path);
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(path, contents)
}

fn main() -> std::io::Result<()> {
    write_stub("python/intensify/__init__.pyi", INIT_PYI)?;
    write_stub("python/intensify/_libintensify.pyi", LIBINTENSIFY_PYI)?;
    Ok(())
}
